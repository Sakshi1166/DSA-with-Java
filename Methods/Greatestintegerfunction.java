package Methods;

public class Greatestintegerfunction {
    public static void main(String[] args) {
        
        //1. Math.ceil() (Ceiling Function): The Math.ceil() method returns the smallest double value that is greater 
        //than or equal to the argument and is equal to a mathematical integer.Essentially, it rounds a number up to the nearest whole number. 
        System.out.println(Math.ceil(4.8));

        //2. Math.floor() (Floor Function): The Math.floor() method returns the largest double value that is less 
        //than or equal to the argument and is equal to a mathematical integer.Essentially, it rounds a number down to the nearest whole number. 
        System.out.print(Math.floor(-3.8));
    }
    
}
