public class incrementdecrement{
    public static void main(String [] args){
        int x = 10;
        System.out.println(x++); //x++ is post increment, prints 10 then increments
        System.out.println(x);
        System.out.println(++x); //++x is pre increment, increments first then prints
        System.out.println(x);

        int y = 12;
        System.out.println(y--); //y-- is post decrement, prints 12 then decrements
        System.out.println(y);
        System.out.println(--y); //--y is pre decrement, decrements first then prints
        System.out.println(y);
        
        



    }
}